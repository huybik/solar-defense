export function getEnemyDef(type: EnemyType, planet?: PlanetId): EnemyDef {
  const def = ENEMY_DEFS[type]
  const variant = planet ? PLANET_ENEMY_VARIANTS[planet]?.[type] : null
  return variant ? { ...def, ...variant } : def
}

export function getPlanetFillerTypes(planet: PlanetId): EnemyType[] {
  return PLANET_FILLER_TYPES[planet] ?? PLANET_FILLER_TYPES.secret
}

export function getMeteorDef(id: string): MeteorDef {
  return METEOR_DEFS[id]
}

export function getTerrainDef(id: string): TerrainDef {
  return TERRAIN_DEFS[id]
}
