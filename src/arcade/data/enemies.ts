import type { DropTable, EnemyDef, EnemyType, MeteorDef, PlanetId, TerrainDef } from '../types'

function credits(min: number, max: number, extra?: DropTable['pickups']): DropTable {
  return {
    credits: [min, max],
    pickups: extra,
  }
}

function variant(sprite: string, altSprites: string[], tint: string): Pick<EnemyDef, 'sprite' | 'altSprites' | 'tint'> {
  return { sprite, altSprites, tint }
}

