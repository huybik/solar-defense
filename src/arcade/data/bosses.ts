import type { BossConfig } from '../types'

function attack(
  id: string,
  label: string,
  pattern: BossConfig['attacks'][number]['bulletPattern'],
  teacherHint: string,
  options: Partial<BossConfig['attacks'][number]> = {},
): BossConfig['attacks'][number] {
  return {
    id,
    label,
    bulletPattern: pattern,
    duration: options.duration ?? 8,
    fireInterval: options.fireInterval ?? 1.2,
    bulletCount: options.bulletCount ?? 10,
    bulletSpeed: options.bulletSpeed ?? 16,
    teacherHint,
    vulnerabilityWindow: options.vulnerabilityWindow,
    hazardType: options.hazardType,
  }
}

