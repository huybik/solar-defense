import type { WeaponDef, WeaponLevelStats, WeaponSlot } from '../types'

const LEVELS = 11

type Curve = number | [number, number] | number[]

interface WeaponBlueprint {
  id: string
  name: string
  slot: WeaponSlot
  description: string
  baseCost: number
  unlockEpisode: number
  projectileType: WeaponDef['projectileType']
  projectileSprite: string
  maxLevel?: number
  recoil?: number
  chargeTime?: number
  burstInterval?: number
  pierce?: number
  waveAmplitude?: number
  waveFrequency?: number
  orbitOffset?: number
  sidekickSprite?: string
  ammoBundle?: number
  trailColor?: string
  synergy?: WeaponDef['synergy']
  tags?: string[]
  levels: {
    fireRate: Curve
    damage: Curve
    projectileCount: Curve
    spread: Curve
    speed: Curve
    energyCost?: Curve
    ammoCost?: Curve
    scale?: Curve
    beamLength?: Curve
    homing?: Curve
    splashRadius?: Curve
    orbitCount?: Curve
    orbitRadius?: Curve
    duration?: Curve
    burstCount?: Curve
  }
}

function valueAt(curve: Curve | undefined, index: number, fallback: number): number {
  if (curve === undefined) return fallback
  if (typeof curve === 'number') return curve
  if (Array.isArray(curve) && curve.length === 2) {
    const [start, end] = curve
    const t = index / (LEVELS - 1)
    return start + (end - start) * t
  }
  if (Array.isArray(curve)) {
    return curve[Math.min(index, curve.length - 1)] ?? fallback
  }
  return fallback
}

function buildLevels(spec: WeaponBlueprint['levels']): WeaponLevelStats[] {
  return Array.from({ length: LEVELS }, (_, index) => ({
    fireRate: valueAt(spec.fireRate, index, 5),
    damage: valueAt(spec.damage, index, 1),
    projectileCount: Math.round(valueAt(spec.projectileCount, index, 1)),
    spread: valueAt(spec.spread, index, 0),
    speed: valueAt(spec.speed, index, 30),
    energyCost: valueAt(spec.energyCost, index, 0),
    ammoCost: Math.round(valueAt(spec.ammoCost, index, 0)),
    scale: valueAt(spec.scale, index, 1),
    beamLength: valueAt(spec.beamLength, index, 10),
    homing: valueAt(spec.homing, index, 0),
    splashRadius: valueAt(spec.splashRadius, index, 0),
    orbitCount: Math.round(valueAt(spec.orbitCount, index, 0)),
    orbitRadius: valueAt(spec.orbitRadius, index, 0),
    duration: valueAt(spec.duration, index, 0),
    burstCount: Math.round(valueAt(spec.burstCount, index, 0)),
  }))
}

function createWeapon(blueprint: WeaponBlueprint): WeaponDef {
  return {
    ...blueprint,
    maxLevel: blueprint.maxLevel ?? LEVELS,
    levels: buildLevels(blueprint.levels),
  }
}

