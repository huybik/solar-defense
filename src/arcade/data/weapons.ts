export const WEAPON_DEFS: Record<string, WeaponDef> = Object.fromEntries(
  [...FRONT_WEAPONS, ...REAR_WEAPONS, ...SIDEKICKS, ...SPECIAL_WEAPONS].map((weapon) => [weapon.id, weapon]),
)

export const WEAPON_LIST = Object.values(WEAPON_DEFS)
const SIDEKICK_WEAPONS = WEAPON_LIST.filter((weapon) => weapon.slot === 'sidekickL')

export const WEAPONS_BY_SLOT: Record<WeaponSlot, WeaponDef[]> = {
  front: WEAPON_LIST.filter((weapon) => weapon.slot === 'front'),
  rear: WEAPON_LIST.filter((weapon) => weapon.slot === 'rear'),
  sidekickL: SIDEKICK_WEAPONS,
  sidekickR: SIDEKICK_WEAPONS,
  special: WEAPON_LIST.filter((weapon) => weapon.slot === 'special'),
}

export function getWeaponDef(id: string | null | undefined): WeaponDef | null {
  if (!id) return null
  return WEAPON_DEFS[id] ?? null
}

export function getWeaponLevel(def: WeaponDef, level: number): WeaponLevelStats {
  return def.levels[Math.max(0, Math.min(level, def.levels.length - 1))]
}
